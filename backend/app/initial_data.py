"""
Initial Data - โหลดข้อมูลเริ่มต้นเมื่อฐานข้อมูลว่าง

================================================================================
วัตถุประสงค์:
================================================================================
เมื่อระบบถูกติดตั้งใหม่ ฐานข้อมูลจะว่างเปล่า
ไฟล์นี้จะสร้างข้อมูลเริ่มต้นเพื่อให้ทดสอบระบบได้ทันที:
- Admin account 1 ตัว
- หน่วยงาน 35 แห่ง (กระทรวงศึกษาธิการจังหวัดนครราชสีมา)
- Manager account 1 ตัวต่อหน่วยงาน
- โครงการตัวอย่าง 3 โครงการ

================================================================================
วิธีใช้:
================================================================================
ถูกเรียกใช้ใน create_app() ใน __init__.py
โดยจะทำงานเฉพาะเมื่อ Org.query.first() เป็น None
(ก็คือฐานข้อมูลว่างเปล่า)

================================================================================
ข้อมูลที่สร้าง:
================================================================================
1. Admin:
   - username: "admin"
   - password: ADMIN_PIN (สุ่ม หรือกำหนดจาก env)
   
2. หน่วยงาน 35 แห่ง:
   - ID: org-001 ถึง org-035
   - ชื่อ: สำนักงานคณะกรรมการการศึกษาขั้นพื้นฐาน, สพป.นม.1-9, มหาวิทยาลัยต่างๆ ฯลฯ
   
3. Manager 35 ตัว:
   - username: "mgr-org-001", "mgr-org-002", ...
   - password: PIN ของหน่วยงานที่สังกัด
   
4. โครงการตัวอย่าง 3 โครงการ:
   - โครงการพัฒนาทักษะดิจิทัลสำหรับครู (org-023)
   - โครงการส่งเสริมการอ่านออกเขียนได้ (org-002)
   - โครงการพัฒนาทักษะอาชีพ (org-012)
"""
from __future__ import annotations

from datetime import datetime, timedelta

from flask import current_app

from .database import db
from .models import Org, Project, User
from .utils import generate_unique_org_pin, random_pin_6


def load_if_empty() -> None:
    """
    โหลดข้อมูลเริ่มต้นเมื่อฐานข้อมูลว่าง
    
    ฟังก์ชันนี้จะถูกเรียกใช้ทุกครั้งที่ start server
    แต่จะทำงานเฉพาะครั้งแรกเท่านั้น (เมื่อฐานข้อมูลว่าง)
    
    ขั้นตอนการทำงาน:
    1. ตรวจสอบว่าฐานข้อมูลว่างหรือไม่
    2. ถ้าไม่ว่าง → ไม่ต้องทำอะไร (ออกไปทันที)
    3. ถ้าว่าง → สร้างข้อมูลเริ่มต้น:
       a. สร้าง Admin account
       b. สร้างหน่วยงาน 35 แห่ง + PIN
       c. สร้าง Manager account 35 ตัว
       d. สร้างโครงการตัวอย่าง 3 โครงการ
       e. Commit ข้อมูลลงฐานข้อมูล
    """
    # ตรวจสอบว่าฐานข้อมูลว่างหรือไม่
    # Org.query.first() คือ SELECT * FROM orgs LIMIT 1
    # ถ้าได้ None แสดงว่ายังไม่มีข้อมูล
    if Org.query.first() is not None:
        return

    # ===== สร้าง Admin Account =====
    # ดึงค่า ADMIN_PIN จาก config
    # สามารถกำหนดได้ผ่าน env ADMIN_PIN
    admin_cfg = str(current_app.config.get("ADMIN_PIN", "") or "").strip()
    
    # ถ้ามีการกำหนด ADMIN_PIN ใน config
    # และเป็นตัวเลข 6 หลัก → ใช้ค่านั้น
    # ถ้าไม่กำหนด → สุ่ม PIN ใหม่
    if admin_cfg and len(admin_cfg) == 6 and admin_cfg.isdigit():
        admin_pin = admin_cfg
    else:
        admin_pin = random_pin_6()
    
    # เก็บ PIN ไว้แสดงใน banner ตอนรัน server
    # (เพื่อให้ admin รู้ว่า PIN คืออะไร)
    current_app.config["_SEEDED_ADMIN_PIN"] = admin_pin

    # สร้าง set เพื่อเก็บ PIN ที่ใช้ไปแล้ว
    # เพื่อป้องกันการซ้ำกัน
    used_pins: set[str] = {admin_pin}

    # ===== สร้างหน่วยงาน 35 แห่ง =====
    # รายชื่อหน่วยงานในจังหวัดนครราชสีมา
    # (เป็นตัวอย่างข้อมูลจริง)
    orgs = [
        # สำนักงานคณะกรรมการการศึกษาขั้นพื้นฐาน (สพฐ.)
        ("org-001", "สำนักงานคณะกรรมการการศึกษาขั้นพื้นฐาน"),
        # สพป.นม.1-9 (สำนักงานเขตพื้นที่การศึกษาประถมศึกษานครราชสีมา)
        ("org-002", "สพป.นม.1"),
        ("org-003", "สพป.นม.2"),
        ("org-004", "สพป.นม.3"),
        ("org-005", "สพป.นม.4"),
        ("org-006", "สพป.นม.5"),
        ("org-007", "สพป.นม.6"),
        ("org-008", "สพป.นม.7"),
        ("org-009", "สพป.นม."),
        # หน่วยงานอื่นๆ
        ("org-010", "ศูนย์การศึกษาพิเศษ เขตการศึกษา 11 จังหวัดนครราชสีมา"),
        ("org-011", "โรงเรียนนครราชสีมาปัญญานุกูล"),
        ("org-012", "สำนักงานอาชีวศึกษาจังหวัดนครราชสีมา"),
        ("org-013", "สำนักงานคณะกรรมการส่งเสริมการศึกษาเอกชน"),
        ("org-014", "สำนักงานส่งเสริมการเรียนรู้จังหวัดนครราชสีมา"),
        ("org-015", "สำนักงานส่งเสริมการปกครองท้องถิ่นจังหวัดนครราชสีมา"),
        ("org-016", "สำนักงานพระพุทธศาสนาจังหวัดนครราชสีมา"),
        ("org-017", "วิทยาลัยนาฏศิลปนครราชสีมา"),
        # มหาวิทยาลัย
        ("org-018", "กระทรวงอุดมศึกษา วิทยาศาสตร์ วิจัย และนวัตกรรม"),
        ("org-019", "โรงเรียนสาธิตมหาวิทยาลัยราชภัฏนครราชสีมา"),
        ("org-020", "โรงเรียนสุรวิวัฒน์ มหาวิทยาลัยเทคโนโลยีสุรนารี"),
        ("org-021", "มหาวิทยาลัยเทคโนโลยีสุรนารี"),
        ("org-022", "มหาวิทยาลัยเทคโนโลยีราชมงคลอีสาน"),
        ("org-023", "มหาวิทยาลัยราชภัฏนครราชสีมา"),
        ("org-024", "มหาวิทยาลัยมหาจุฬาลงกรณราชวิทยาลัย วิทยาเขตนครราชสีมา"),
        ("org-025", "วิทยาลัยศาสนศาสตร์นครราชสีมา มหาวิทยาลัยมหามกุฏราชวิทยาลัย"),
        ("org-026", "มหาวิทยาลัยรามคำแหง สาขาวิทยบริการเฉลิมพระเกียรตินครราชสีมา"),
        ("org-027", "สถาบันบัณฑิตพัฒนบริหารศาสตร์ คณะรัฐประศาสนศาสตร์ ฯ"),
        ("org-028", "มหาวิทยาลัยวงษ์ชวลิตกุล"),
        ("org-029", "วิทยาลัยนครราชสีมา"),
        ("org-030", "วิทยาลัยเทคโนโลยีพนมวันท์"),
        ("org-031", "วิทยาลัยพยาบาลบรมราชชนนีนครราชสีมา"),
        ("org-032", "สำนักงานพัฒนาสังคมและความมั่นคงของมนุษย์จังหวัดนครราชสีมา"),
        ("org-033", "สำนักงานสาธารณสุขจังหวัดนครราชสีมา"),
        ("org-034", "สำนักงานสถิติจังหวัดนครราชสีมา"),
        ("org-035", "สำนักงานศึกษาธิการจังหวัดนครราชสีมา"),
    ]

    # สร้าง PIN และ Org object สำหรับแต่ละหน่วยงาน
    org_pins: dict[str, str] = {}
    for oid, name in orgs:
        # สร้าง PIN ที่ไม่ซ้ำกับ PIN ที่ใช้ไปแล้ว
        pin = generate_unique_org_pin(used_pins)
        used_pins.add(pin)  # เพิ่ม PIN นี้เข้าไปใน set
        org_pins[oid] = pin  # เก็บ PIN ไว้ใช้สร้าง Manager
        
        # สร้าง Org object และเพิ่มลง session
        db.session.add(Org(id=oid, name=name, active=True, pin=pin))

    # ===== สร้าง Admin User =====
    # Admin ไม่สังกัดหน่วยงานใด (org_id = None)
    db.session.add(User(
        id="u-admin",
        username="admin",
        password=admin_pin,
        role="admin",
        org_id=None,
        active=True
    ))

    # ===== สร้าง Manager User สำหรับแต่ละหน่วยงาน =====
    for oid, _ in orgs:
        mgr_pin = org_pins[oid]  # PIN ของ Manager ตรงกับ PIN ของ Org
        db.session.add(User(
            id=f"u-mgr-{oid}",
            username=f"mgr-{oid}",  # เช่น "mgr-org-001"
            password=mgr_pin,
            role="manager",
            org_id=oid,  # สังกัดหน่วยงานนี้
            active=True
        ))

    # ===== สร้างโครงการตัวอย่าง 3 โครงการ =====
    # กำหนดวันที่สำหรับโครงการ
    base = datetime.utcnow()
    
    projects = [
        # (id, org_id, title, budget, sdg, updated_by)
        ("p-1", "org-023", "โครงการพัฒนาทักษะดิจิทัลสำหรับครู", 250000, ["4.4", "4.c"], "admin"),
        ("p-2", "org-002", "โครงการส่งเสริมการอ่านออกเขียนได้", 180000, ["4.1", "4.6"], "mgr-org-002"),
        ("p-3", "org-012", "โครงการพัฒนาทักษะอาชีพ", 280000, ["4.4"], "mgr-org-012"),
    ]
    
    for i, (pid, oid, title, budget, sdg, ub) in enumerate(projects):
        # สร้างวันที่สร้างและวันที่แก้ไข
        # แต่ละโครงการจะมีวันที่ต่างกันเล็กน้อย (ลดลง 1 วันต่อโครงการ)
        created = base - timedelta(days=10 - i)
        
        # สร้าง Project object
        p = Project(
            id=pid,
            org_id=oid,
            title=title,
            budget=budget,
            objective="",
            policy="",
            owner="ผู้รับผิดชอบ",
            year=2569,  # ปีงบประมาณ
            start_date=created.date(),
            end_date=(created + timedelta(days=180)).date(),  # ดำเนินการ 180 วัน
            sdg=sdg,  # SDG targets
            created_at=created,
            updated_at=base - timedelta(days=1),  # แก้ไขล่าสุดเมื่อวาน
            updated_by=ub  # ผู้แก้ไข
        )
        db.session.add(p)

    # ===== Commit ข้อมูลทั้งหมดลงฐานข้อมูล =====
    # ถ้าไม่ commit ข้อมูลจะไม่ถูกบันทึก
    db.session.commit()
