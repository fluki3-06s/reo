"""
Database Module - จัดการการเชื่อมต่อฐานข้อมูล

================================================================================
วัตถุประสงค์:
================================================================================
ไฟล์นี้ใช้สำหรับสร้าง SQLAlchemy instance ที่จะถูกใช้งานทั่วทั้งระบบ
โดยจะถูก import จากที่อื่นในระบบเพื่อเข้าถึง database

================================================================================
การใช้งาน:
================================================================================
- ในไฟล์ __init__.py ของ app จะเรียก db.init_app(flask_app) เพื่อเชื่อมต่อ
- ในไฟล์ models.py จะใช้ db.Model เป็น base class สำหรับทำ ORM mapping
- ในไฟล์ routes ต่างๆ จะใช้ db.session เพื่อจัดการ database transactions

================================================================================
Database: SQLite
================================================================================
ระบบนี้ใช้ SQLite เป็น database เนื่องจาก:
- ไม่ต้องติดตั้ง database server แยก
- เหมาะสำหรับ development และระบบขนาดเล็ก-กลาง
- ข้อมูลเก็บในไฟล์: backend/instance/sdg4.db
"""
from __future__ import annotations

from flask_sqlalchemy import SQLAlchemy

# สร้าง SQLAlchemy instance โดยยังไม่ได้ผูกกับ Flask app
# จะถูกผูกด้วย db.init_app(app) ใน create_app()
db: SQLAlchemy = SQLAlchemy()
