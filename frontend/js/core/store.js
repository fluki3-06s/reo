/**
 * ================================================================================
 * Data Store - จัดการข้อมูลใน Client-side
 * ================================================================================
 * 
 * ไฟล์นี้จัดการ Session และ Local Storage
 * 
 * ================================================================================
 * Session vs Local Storage:
 * ================================================================================
 * 
 * Session (window.session):
 * - เก็บข้อมูลผู้ใช้ปัจจุบัน
 * - อยู่ใน JavaScript memory
 * - หายไปเมื่อปิด tab
 * 
 * Local Storage (localStorage):
 * - เก็บ session สำหรับ restore
 * - อยู่ใน browser storage
 * - อยู่คงถาวรจนกว่าจะ clear
 * 
 * ================================================================================
 * Session Structure:
 * ================================================================================
 * 
 * Admin Session:
 * {
 *   role: "admin",
 *   userId: "u-admin",
 *   orgId: null
 * }
 * 
 * Manager Session:
 * {
 *   role: "manager",
 *   userId: "u-mgr-org-001",
 *   orgId: "org-001"
 * }
 * 
 * ================================================================================
 */

// ================================================================================
// Global Variables
// ================================================================================
// window.DB     - ข้อมูลทั้งหมดในระบบ (จาก api.getFullData)
// window.session - ข้อมูลผู้ใช้ปัจจุบัน
// ================================================================================

// ================================================================================
// ฟังก์ชัน: loadSession
// ================================================================================
// อ่าน session จาก localStorage
// 
// Returns:
//   session object ถ้ามี
//   null ถ้าไม่มีหรือ parse ไม่ได้
// ================================================================================
function loadSession() {
  // อ่าน session จาก localStorage
  const raw = localStorage.getItem(SESSION_KEY);
  if (!raw) return null;
  
  // แปลง JSON string เป็น object
  try {
    return JSON.parse(raw);
  } catch {
    return null;
  }
}

// ================================================================================
// ฟังก์ชัน: migrateSession
// ================================================================================
// แปลง session format เก่าให้เป็น format ใหม่
// 
// เหตุผล:
// - Session format อาจเปลี่ยนไปตาม version
// - ต้อง migrate เพื่อให้ backward compatible
// 
// Migration:
// - session ที่มี userId อยู่แล้ว → ใช้ตรงๆ
// - session เก่าที่มีแค่ role + orgId → หา user จริงจาก DB
// 
// Returns:
//   session object ที่ valid
//   null ถ้า migrate ไม่ได้
// ================================================================================
function migrateSession(s) {
  // ถ้าไม่มี session หรือไม่เป็น object → return null
  if (!s || typeof s !== 'object') return null;
  
  // ถ้ามี userId อยู่แล้ว (format ใหม่) → ใช้ตรงๆ
  if (s.userId) return s;

  // ========================================================================
  // Migration สำหรับ Admin Session (format เก่า)
  // ========================================================================
  if (s.role === 'admin') {
    // หา admin user จาก DB
    const adminUser = (DB?.users || []).find(u => u.role === 'admin' && u.active !== false);
    return adminUser ? { role: 'admin', userId: adminUser.id } : null;
  }
  
  // ========================================================================
  // Migration สำหรับ Manager Session (format เก่า)
  // ========================================================================
  if (s.role === 'manager' && s.orgId) {
    // หา manager ที่มี orgId ตรงกัน
    const mgr = (DB?.users || []).find(u => u.role === 'manager' && u.orgId === s.orgId && u.active !== false);
    return mgr ? { role: 'manager', userId: mgr.id, orgId: mgr.orgId } : null;
  }
  
  return null;
}

// ================================================================================
// ฟังก์ชัน: saveSession
// ================================================================================
// บันทึก session ลง localStorage
// 
// Args:
//   s - session object ที่ต้องการบันทึก
// ================================================================================
function saveSession(s) {
  localStorage.setItem(SESSION_KEY, JSON.stringify(s));
}

// ================================================================================
// ฟังก์ชัน: clearSession
// ================================================================================
// ลบ session ออกจาก localStorage
// ================================================================================
function clearSession() {
  localStorage.removeItem(SESSION_KEY);
}

