/**
 * ================================================================================
 * Utilities - ฟังก์ชันช่วยเหลือ
 * ================================================================================
 * 
 * ไฟล์นี้รวบรวมฟังก์ชันช่วยเหลือที่ใช้ทั่วทั้งระบบ
 * 
 * ================================================================================
 * ฟังก์ชันที่มี:
 * ================================================================================
 * 
 * - randomId()         - สร้าง ID แบบสุ่ม
 * - formatMoney()      - จัดรูปแบบตัวเลขเป็นเงินบาท
 * - formatDate()       - จัดรูปแบบวันที่เป็นภาษาไทย
 * - getOrgName()       - ดึงชื่อหน่วยงานจาก ID
 * - getDisplayNameFromUsername() - ดึงชื่อแสดงจาก username
 * - getCurrentUser()    - ดึงข้อมูลผู้ใช้ปัจจุบัน
 * - getCurrentUsername() - ดึง username ของผู้ใช้ปัจจุบัน
 * - canEdit()          - ตรวจสอบว่ามีสิทธิ์แก้ไขโครงการหรือไม่
 * - fileToDataURL()     - แปลงไฟล์เป็น base64 data URL
 * 
 * ================================================================================
 */

// ================================================================================
// ฟังก์ชัน: randomId
// ================================================================================
// สร้าง ID แบบสุ่มที่ไม่ซ้ำกัน
// 
// Format: prefix-timestamp-random
// ตัวอย่าง: "img-a1b2c3d4e5f6-20231201123456abcdef"
// 
// Args:
//   prefix - คำนำหน้า เช่น "img", "p", "org"
// 
// Returns:
//   string ID ที่สุ่ม
// ================================================================================
function randomId(prefix) {
  return prefix + '-' + Math.random().toString(36).substr(2, 9) + Date.now().toString(36);
}

// ================================================================================
// ฟังก์ชัน: formatMoney
// ================================================================================
// จัดรูปแบบตัวเลขเป็นเงินบาท (ไทย)
// 
// ตัวอย่าง:
//   1000000 → "1,000,000"
//   1234567 → "1,234,567"
// 
// Args:
//   n - ตัวเลข
// 
// Returns:
//   string ที่จัดรูปแบบแล้ว
// ================================================================================
function formatMoney(n) {
  // Number(n || 0) คือแปลง n เป็นตัวเลข ถ้า n เป็น null/undefined ใช้ 0
  // toLocaleString('th-TH') คือจัดรูปแบบตาม locale ไทย
  return Number(n || 0).toLocaleString('th-TH');
}

// ================================================================================
// ฟังก์ชัน: formatDate
// ================================================================================
// จัดรูปแบบวันที่เป็นภาษาไทย
// 
// ตัวอย่าง:
//   "2025-01-15" → "15 มกราคม 2569"
// 
// Args:
//   dateStr - date string รูปแบบ YYYY-MM-DD
// 
// Returns:
//   string ที่จัดรูปแบบแล้ว หรือ "-" ถ้าไม่มีค่า
// ================================================================================
function formatDate(dateStr) {
  if (!dateStr) return '-';
  
  // เพิ่ม T00:00:00 เพื่อให้เป็น midnight (ป้องกัน timezone shift)
  const d = new Date(dateStr + 'T00:00:00');
  
  // toLocaleDateString('th-TH') จัดรูปแบบตาม locale ไทย
  return d.toLocaleDateString('th-TH', { year: 'numeric', month: 'long', day: 'numeric' });
}

// ================================================================================
// ฟังก์ชัน: getOrgName
// ================================================================================
// ดึงชื่อหน่วยงานจาก orgId
// 
// Args:
//   orgId - รหัสหน่วยงาน
// 
// Returns:
//   ชื่อหน่วยงาน หรือ "-" ถ้าไม่พบ
// ================================================================================
function getOrgName(orgId) {
  // "admin" ไม่มีหน่วยงาน
  if (orgId === 'admin') return 'Admin';
  
  // หาหน่วยงานจาก window.DB.orgs
  const org = DB?.orgs?.find(o => o.id === orgId);
  return org ? org.name : '-';
}

// ================================================================================
// ฟังก์ชัน: getDisplayNameFromUsername
// ================================================================================
// ดึงชื่อแสดงที่ friendly จาก username
// 
// ตัวอย่าง:
//   "admin" → "Admin"
//   "mgr-org-001" → "สพป.นม.1" (ชื่อหน่วยงาน)
//   "unknown" → "unknown"
// 
// Args:
//   username - username
// 
// Returns:
//   ชื่อแสดงที่ friendly
// ================================================================================
function getDisplayNameFromUsername(username) {
  if (!username) return '-';
  if (username === 'admin') return 'Admin';

  // ถ้าเป็น manager (เริ่มต้นด้วย "mgr-")
  if (username.startsWith('mgr-')) {
    // หา user จาก DB
    const user = DB?.users?.find(u => u.username === username && u.orgId);
    if (user && user.orgId) {
      // แสดงชื่อหน่วยงานแทน username
      return getOrgName(user.orgId);
    }
  }

  return username;
}

// ================================================================================
// ฟังก์ชัน: getCurrentUser
// ================================================================================
// ดึงข้อมูลผู้ใช้ปัจจุบัน
// 
// Returns:
//   User object ถ้าล็อกอินแล้ว
//   null ถ้ายังไม่ล็อกอิน
// ================================================================================
function getCurrentUser() {
  // ดึง userId จาก session
  if (!session?.userId) return null;
  
  // หาข้อมูล user จาก DB
  return (DB?.users || []).find(u => u.id === session.userId) || null;
}

// ================================================================================
// ฟังก์ชัน: getCurrentUsername
// ================================================================================
// ดึง username ของผู้ใช้ปัจจุบัน
// 
// Returns:
//   username ของผู้ใช้
//   "admin" ถ้าเป็น admin
//   "unknown" ถ้ายังไม่ล็อกอิน
// ================================================================================
function getCurrentUsername() {
  const u = getCurrentUser();
  return u?.username || (session?.role === 'admin' ? 'admin' : 'unknown');
}

// ================================================================================
// ฟังก์ชัน: canEdit
// ================================================================================
// ตรวจสอบว่าผู้ใช้ปัจจุบันมีสิทธิ์แก้ไขโครงการหรือไม่
// 
// Logic:
// - Admin: แก้ไขได้ทุกโครงการ
// - Manager: แก้ไขได้เฉพาะโครงการของหน่วยงานตัวเอง
// 
// Args:
//   project - Project object
// 
// Returns:
//   true ถ้ามีสิทธิ์
//   false ถ้าไม่มีสิทธิ์
// ================================================================================
function canEdit(project) {
  // ต้องล็อกอินก่อน
  if (!session) return false;
  
  // Admin แก้ไขได้ทุกอย่าง
  if (session.role === 'admin') return true;
  
  // Manager แก้ไขได้เฉพาะโครงการของหน่วยงานตัวเอง
  if (session.role === 'manager') return project?.orgId === session.orgId;
  
  return false;
}

// ================================================================================
// ฟังก์ชัน: fileToDataURL
// ================================================================================
// แปลงไฟล์เป็น base64 data URL
// 
// ใช้สำหรับ:
// - อัปโหลดรูปภาพ (แปลงเป็น base64 ก่อนส่งไป backend)
// 
// Args:
//   file - File object จาก input[type=file]
// 
// Returns:
//   Promise<string> - base64 data URL
// 
// ตัวอย่าง:
//   "data:image/jpeg;base64,/9j/4AAQ..."
// ================================================================================
function fileToDataURL(file) {
  return new Promise((resolve, reject) => {
    // FileReader สำหรับอ่านไฟล์
    const reader = new FileReader();
    
    // เมื่ออ่านเสร็จ
    reader.onload = () => resolve(reader.result);
    
    // เมื่อเกิด error
    reader.onerror = reject;
    
    // อ่านไฟล์เป็น data URL (base64)
    reader.readAsDataURL(file);
  });
}
