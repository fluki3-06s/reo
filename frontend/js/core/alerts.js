/**
 * ================================================================================
 * Alerts - ฟังก์ชันแสดงข้อความแจ้งเตือน (SweetAlert2)
 * ================================================================================
 * 
 * ไฟล์นี้ใช้ SweetAlert2 สำหรับแสดงข้อความแจ้งเตือน
 * 
 * ================================================================================
 * SweetAlert2:
 * ================================================================================
 * เป็น JavaScript library สำหรับสร้าง alert, confirm, toast ที่สวยงาม
 * แทน alert() ธรรมดาของ browser
 * 
 * ================================================================================
 * ฟังก์ชันที่มี:
 * ================================================================================
 * 
 * - showSuccess()  - แสดง toast สำเร็จ (สีเขียว)
 * - showError()    - แสดง toast ผิดพลาด (สีแดง)
 * - showInfo()     - แสดง toast ข้อมูล (สีน้ำเงิน)
 * - showWarning()  - แสดง toast เตือน (สีเหลือง)
 * - confirm()      - แสดง dialog ยืนยัน (มีปุ่ม ยืนยัน/ยกเลิก)
 * - getSoftBadgeColors() - แปลงสี hex เป็นสีที่อ่อนกว่า (สำหรับ badge)
 * 
 * ================================================================================
 */

// ================================================================================
// Toast Configuration
// ================================================================================
// กำหนดค่าเริ่มต้นสำหรับ Toast
// 
// Toast คือข้อความที่แสดงชั่วคราวที่มุมขวาบน แล้วหายไปเอง
// ================================================================================
const Toast = Swal.mixin({
  toast: true,              // แสดงเป็น toast (ไม่ใช่ modal)
  position: 'top-end',      // ตำแหน่ง: ขวาบน
  showConfirmButton: false, // ไม่แสดงปุ่มยืนยัน
  timer: 3000,             // หายไปหลัง 3 วินาที
  timerProgressBar: true,   // แสดง progress bar
});

// ================================================================================
// ฟังก์ชัน: showSuccess
// ================================================================================
// แสดง Toast สำเร็จ (สีเขียว)
// 
// ใช้เมื่อ:
// - บันทึกสำเร็จ
// - ลบสำเร็จ
// - เพิ่มสำเร็จ
// 
// Args:
//   title - หัวข้อ (required)
//   text  - รายละเอียด (optional)
// ================================================================================
function showSuccess(title, text = '') {
  Toast.fire({ icon: 'success', title, text });
}

// ================================================================================
// ฟังก์ชัน: showError
// ================================================================================
// แสดง Toast ผิดพลาด (สีแดง)
// 
// ใช้เมื่อ:
// - เกิด error
// - ไม่สามารถทำงานได้
// - validation fail
// 
// Args:
//   title - หัวข้อ (required)
//   text  - รายละเอียด (optional)
// ================================================================================
function showError(title, text = '') {
  Toast.fire({ icon: 'error', title, text });
}

// ================================================================================
// ฟังก์ชัน: showInfo
// ================================================================================
// แสดง Toast ข้อมูล (สีน้ำเงิน)
// 
// ใช้เมื่อ:
// - แจ้งข้อมูลทั่วไป
// - ยืนยันการทำงาน
// 
// Args:
//   title - หัวข้อ (required)
//   text  - รายละเอียด (optional)
// ================================================================================
function showInfo(title, text = '') {
  Toast.fire({ icon: 'info', title, text });
}

// ================================================================================
// ฟังก์ชัน: showWarning
// ================================================================================
// แสดง Toast เตือน (สีเหลือง)
// 
// ใช้เมื่อ:
// - validation ไม่ผ่านบางส่วน
// - คำเตือนทั่วไป
// 
// Args:
//   title - หัวข้อ (required)
//   text  - รายละเอียด (optional)
// ================================================================================
function showWarning(title, text = '') {
  Toast.fire({ icon: 'warning', title, text });
}

// ================================================================================
// ฟังก์ชัน: getSoftBadgeColors
// ================================================================================
// แปลงสี hex เป็นสีที่อ่อนกว่า (สำหรับ badge/label)
// 
// ใช้สำหรับ:
// - ทำ badge สีตาม SDG target
// - สีพื้นหลัง = สีเดิมผสมขาว (85% ขาว + 15% สีเดิม)
// - สีตัวอักษร = สีเดิมเข้มขึ้น (60% ของสีเดิม)
// 
// Args:
//   hexColor - สี hex เช่น "#FF6B6B"
// 
// Returns:
//   { bg: "...", text: "..." } - CSS color values
// ================================================================================
function getSoftBadgeColors(hexColor) {
  // ถ้าไม่มีสีหรือเป็นสีเทา → ใช้สี neutral
  if (!hexColor || hexColor === '#ccc') {
    return { bg: 'var(--badge-neutral-bg)', text: 'var(--badge-neutral-text)' };
  }

  // แปลง hex เป็น RGB
  // hex.slice(1, 3) = Red (2 ตัวแรก)
  // hex.slice(3, 5) = Green (2 ตัวกลาง)
  // hex.slice(5, 7) = Blue (2 ตัวสุดท้าย)
  const r = parseInt(hexColor.slice(1, 3), 16);
  const g = parseInt(hexColor.slice(3, 5), 16);
  const b = parseInt(hexColor.slice(5, 7), 16);

  // สีพื้นหลัง = 15% สีเดิม + 85% ขาว (ทำให้อ่อนลง)
  const bgR = Math.round(r * 0.15 + 255 * 0.85);
  const bgG = Math.round(g * 0.15 + 255 * 0.85);
  const bgB = Math.round(b * 0.15 + 255 * 0.85);
  const bgColor = `rgb(${bgR}, ${bgG}, ${bgB})`;

  // สีตัวอักษร = 60% ของสีเดิม (ทำให้เข้มขึ้น)
  const textR = Math.round(r * 0.6);
  const textG = Math.round(g * 0.6);
  const textB = Math.round(b * 0.6);
  const textColor = `rgb(${textR}, ${textG}, ${textB})`;

  return { bg: bgColor, text: textColor };
}

// ================================================================================
// ฟังก์ชัน: confirm
// ================================================================================
// แสดง Dialog ยืนยัน
// 
// ใช้เมื่อ:
// - ต้องการให้ผู้ใช้ยืนยันก่อนทำงานสำคัญ
// - เช่น ลบข้อมูล, ออกจากระบบ
// 
// Args:
//   title         - หัวข้อ
//   text          - รายละเอียด
//   confirmText   - ข้อความปุ่มยืนยัน (default: "ยืนยัน")
// 
// Returns:
//   Promise<boolean>
//   true  = ผู้ใช้กด ยืนยัน
//   false = ผู้ใช้กด ยกเลิก
// ================================================================================
async function confirm(title, text, confirmText = 'ยืนยัน') {
  const result = await Swal.fire({
    title,
    text,
    icon: 'question',       // ไอคอนเครื่องหมายคำถาม
    showCancelButton: true, // แสดงปุ่มยกเลิก
    confirmButtonText: confirmText,
    cancelButtonText: 'ยกเลิก',
    confirmButtonColor: '#2563eb', // สีน้ำเงิน
    cancelButtonColor: '#d33',     // สีแดง
  });
  
  // result.isConfirmed = true ถ้ากดยืนยัน
  return result.isConfirmed;
}
