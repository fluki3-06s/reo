/**
 * ================================================================================
 * Login - หน้าจอเข้าสู่ระบบ
 * ================================================================================
 * 
 * ไฟล์นี้จัดการการ Login/Logout ของระบบ
 * 
 * ================================================================================
 * การทำงาน:
 * ================================================================================
 * 
 * Login Flow:
 * 1. แสดงรายชื่อหน่วยงานใน dropdown
 * 2. ผู้ใช้เลือกหน่วยงาน + กรอก PIN
 * 3. เรียก API /api/auth/login
 * 4. ถ้าสำเร็จ → เก็บ session + โหลดข้อมูล + เปิดหน้าหลัก
 * 5. ถ้าไม่สำเร็จ → แสดง error
 * 
 * Logout Flow:
 * 1. ผู้ใช้คลิกปุ่มออกจากระบบ
 * 2. แสดง confirm dialog
 * 3. เรียก API /api/auth/logout
 * 4. Reload หน้า
 * 
 * ================================================================================
 */

// ================================================================================
// ฟังก์ชัน: initLogin
// ================================================================================
// เริ่มต้นหน้า Login
// 
// ทำงาน:
// 1. เพิ่ม option "Admin" ใน dropdown
// 2. เพิ่ม option หน่วยงานทั้งหมด (จาก window.DB.orgs)
// 3. เลือกหน่วยงานที่เคยล็อกอินล่าสุด
// 4. แสดง Admin PIN (สำหรับระบบ Demo)
// 
// หมายเหตุ:
// - ถูกเรียกจาก app.js เมื่อยังไม่ล็อกอิน
// - window.DB มาจากการเรียก api.getOrgsPublic() ก่อนหน้า
// ================================================================================
async function initLogin() {
  // ค้นหา dropdown สำหรับเลือกหน่วยงาน
  const select = document.getElementById('loginOrg');
  if (!select) return; // ถ้าไม่พบ element ออกไป
  
  // เพิ่ม option แรกสำหรับ Admin
  select.innerHTML = '<option value="admin">Admin</option>';

  // ========================================================================
  // เพิ่ม option สำหรับหน่วยงานทั้งหมด
  // ========================================================================
  // กรองเฉพาะหน่วยงานที่ active (ไม่ถูกปิดใช้งาน)
  (DB.orgs || []).filter(o => o.active !== false).forEach(org => {
    // สร้าง <option> element
    const opt = document.createElement('option');
    opt.value = org.id;      // ใช้ org.id เป็น value
    opt.textContent = org.name; // ใช้ org.name เป็น text
    select.appendChild(opt);
  });

  // ========================================================================
  // เลือกหน่วยงานที่ล็อกอินล่าสุด
  // ========================================================================
  // ใช้ localStorage เก็บ last org ที่ล็อกอิน
  // ทำให้ผู้ใช้ไม่ต้องเลือกหน่วยงานใหม่ทุกครั้ง
  // ========================================================================
  try {
    const last = localStorage.getItem('sdg4_last_org');
    // ตรวจสอบว่า org ที่เก็บไว้ยังมีอยู่ใน dropdown
    // ถ้ามี → เลือกให้อัตโนมัติ
    if (last && select.querySelector(`option[value="${last}"]`)) {
      select.value = last;
    }
  } catch (_) {}

  // ========================================================================
  // แสดง Admin PIN (สำหรับระบบ Demo)
  // ========================================================================
  // เรียก API ดึง admin PIN และแสดงในหน้า login
  // ========================================================================
  const adminPinHint = document.getElementById('adminPinHint');
  const adminPinDisplay = document.getElementById('adminPinDisplay');
  
  try {
    const adminPin = await api.getAdminPin();
    if (adminPin && adminPinDisplay) {
      adminPinDisplay.textContent = adminPin;
      if (adminPinHint) adminPinHint.classList.remove('hidden');
    }
  } catch (e) {
    console.log('ไม่สามารถดึง Admin PIN:', e);
  }

  // ========================================================================
  // Event: แสดง/ซ่อน Admin PIN ตามการเลือกหน่วยงาน
  // ========================================================================
  select.addEventListener('change', async () => {
    const isAdmin = select.value === 'admin';
    
    if (isAdmin) {
      // ถ้าเลือก Admin → แสดง PIN
      if (!adminPinDisplay.textContent || adminPinDisplay.textContent === 'กำลังโหลด...') {
        try {
          const pin = await api.getAdminPin();
          if (pin) adminPinDisplay.textContent = pin;
        } catch (_) {}
      }
      if (adminPinHint) adminPinHint.classList.remove('hidden');
    } else {
      // ถ้าเลือกหน่วยงานอื่น → ซ่อน PIN
      if (adminPinHint) adminPinHint.classList.add('hidden');
    }
  });
}

// ================================================================================
// Event: Login Button Click
// ================================================================================
// เมื่อผู้ใช้คลิกปุ่ม "เข้าสู่ระบบ"
// ================================================================================
document.getElementById('loginBtn')?.addEventListener('click', async () => {
  // ดึงค่าจาก form
  const orgId = document.getElementById('loginOrg')?.value;
  const pin = document.getElementById('loginPin')?.value.trim();

  // ========================================================================
  // Validation: ตรวจสอบ PIN
  // ========================================================================
  // PIN ต้องเป็นตัวเลข 6 หลักพอดี
  // regex: ^\d{6}$
  // ========================================================================
  if (!/^\d{6}$/.test(pin || '')) {
    showWarning('PIN ไม่ถูกต้อง', 'กรุณาระบุรหัส PIN 6 หลัก');
    return;
  }

  try {
    // ========================================================================
    // เรียก API Login
    // ========================================================================
    const res = await api.login(orgId, pin);
    
    // เก็บ session ไว้ใน window.session
    window.session = res.session;
    
    // ========================================================================
    // โหลดข้อมูลทั้งหมด
    // ========================================================================
    // หลังจาก login สำเร็จ ต้องโหลดข้อมูลเต็ม (orgs, users, projects, audit)
    // เพื่อให้สามารถใช้งานระบบได้
    // ========================================================================
    const full = await api.getFullData();
    window.DB = { 
      orgs: full.orgs, 
      users: full.users, 
      projects: full.projects, 
      audit: full.audit 
    };
    
    // ========================================================================
    // จดจำหน่วยงานที่ล็อกอิน
    // ========================================================================
    // ถ้าเป็น Manager (ไม่ใช่ admin) → จดจำหน่วยงาน
    // เพื่อเลือกอัตโนมัติครั้งต่อไป
    // ========================================================================
    if (orgId !== 'admin') {
      try { localStorage.setItem('sdg4_last_org', orgId); } catch (_) {}
    }
    
    // ========================================================================
    // แสดง Success และเริ่ม App
    // ========================================================================
    // Admin: "ยินดีต้อนรับ"
    // Manager: "หน่วยงาน: [ชื่อหน่วยงาน]"
    // ========================================================================
    showSuccess(
      'เข้าสู่ระบบสำเร็จ', 
      orgId === 'admin' 
        ? 'ยินดีต้อนรับ' 
        : `หน่วยงาน: ${(DB.orgs || []).find(o => o.id === orgId)?.name || orgId}`
    );
    initApp();
    
  } catch (e) {
    // ========================================================================
    // แสดง Error
    // ========================================================================
    // ส่วนใหญ่จะเป็น "PIN ไม่ถูกต้อง"
    // ========================================================================
    showError('เข้าสู่ระบบไม่สำเร็จ', e.message || 'PIN ไม่ถูกต้อง');
  }
});

// ================================================================================
// Event: Enter Key on PIN Input
// ================================================================================
// เมื่อกด Enter ในช่อง PIN → คลิกปุ่ม Login
// 
// ทำให้ UX ดีขึ้น: กรอก PIN เสร็จกด Enter ได้เลย
// ================================================================================
document.getElementById('loginPin')?.addEventListener('keypress', (e) => {
    if (e.key === 'Enter') {
      document.getElementById('loginBtn').click();
    }
});

// ================================================================================
// Event: Logout Button Click
// ================================================================================
// เมื่อผู้ใช้คลิกปุ่ม "ออกจากระบบ"
// ================================================================================
document.getElementById('logoutBtn')?.addEventListener('click', async () => {
  // ========================================================================
  // แสดง Confirm Dialog
  // ========================================================================
  const ok = await confirm('ออกจากระบบ', 'ต้องการออกจากระบบใช่ไหม?', 'ออกจากระบบ');
  
  if (ok) {
    // ========================================================================
    // ยืนยันการออกจากระบบ
    // ========================================================================
    // เรียก API Logout
    try { await api.logout(); } catch (_) {}
    
    // ล้าง session (ไม่ใช้ session ต่อแล้ว)
    session = null;
    
    // แสดงข้อความ
    showInfo('ออกจากระบบแล้ว', 'ออกจากระบบสำเร็จ');
    
    // Reload หน้า → กลับไปหน้า Login
    location.reload();
  }
});
